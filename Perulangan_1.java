package belajar_java;

public class Perulangan_1 {
public static void main(BelajarString[]args){
	int nilai = 10;
	while(nilai>=1){
		System.out.println(nilai);
		nilai--;
	}
	System.out.println("----------------------------");
	int nilai2 = 1;
	do{
		System.out.println(nilai2);
		nilai2++;
	}while(nilai2<=10);
}
}
